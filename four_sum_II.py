"""
Given four lists A, B, C, D of integer values, compute how many tuples (i, j, k, l) 
there are such that A[i] + B[j] + C[k] + D[l] is zero.
"""
def fourSumCountUtility(values):
    if len(values) == 1:
        return [[x] for x in range(len(values[0]))]

    permutations = fourSumCountUtility(values[1:])
    result = []
    for permutation in permutations:
        for x in range(len(values[0])):
            result.append(permutation + [x])
    return result

def fourSumCount(A, B, C, D):
    # Generate all permutations using indexes
    # See sum using generated indexes
    # Count the number of permutations that result in a sum of zero
    count = 0
    possibilities = fourSumCountUtility([A, B, C, D])
    for indexes in possibilities:
        if A[indexes[0]] + B[indexes[1]] + C[indexes[2]] + D[indexes[3]] == 0:
            count += 1
    return count

